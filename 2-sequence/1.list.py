'''

线性表
是一组元素的序列的抽象
分为顺序表和链表

顺序表：物理内存中开辟一段连续有序的空间，首地址已经分配固定，用index来表示索引，每个元素占用4字节
增：
在头部增加，引起后面数据的移动，效率低
在中间插入，引起后面数据的移动，效率低
在尾部追加，前面数据不需要移动，效率高

删：
头部或者中间删除，后面数据向前移动，效率低
尾部删除，其他数据不需移动，效率高

改：
不会引起数据的移动，直接通过索引定位，效率很高

查：
通过索引定位，效率高


链表
内存中没有顺序存放，但是依然有index，还是有序序列
链表自己记录了head地址，tail地址，和前一个以及后一个的地址
增删改查没有数据的移动，因此效率都比较高

栈，属于LIFO，后进先出
队列，FIFO，先进先出；LIFO，后进先出；优先队列



'''